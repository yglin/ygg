  toText(): string {
    const tmp = document.createElement('DIV');
    tmp.innerHTML = this.content;
    return tmp.textContent || tmp.innerText || '';
  }

  fromJSON(data: any): this {
    if (typeof data === 'string') {
      this.content = data;
    } else if (Html.isHtml(data)) {
      extend(this, data);
    }
    return this;
  }

